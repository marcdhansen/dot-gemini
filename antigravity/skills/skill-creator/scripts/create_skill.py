def to_kebab(name: str) -> str:
    """Normalise name to kebab-case. Raises if result is invalid."""
    kebab = re.sub(r"[\s_]+", "-", name.strip().lower())
    kebab = re.sub(r"[^a-z0-9-]", "", kebab)
    kebab = re.sub(r"-+", "-", kebab).strip("-")
    if not kebab:
        raise ValueError(f"Cannot derive a valid kebab-case name from: {name!r}")
    if kebab != name.strip().lower().replace("_", "-").replace(" ", "-"):
        print(f"  Note: name normalised to '{kebab}'")
    return kebab


