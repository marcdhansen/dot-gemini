def is_non_interactive() -> bool:
    return (
        not sys.stdin.isatty() or
        bool(os.getenv("CI")) or
        bool(os.getenv("GITHUB_ACTIONS")) or
        bool(os.getenv("AUTOMATED_MODE"))
    )


def safe_input(prompt: str, default: str | None = None,
               choices: list[str] | None = None) -> str:
    if is_non_interactive():
        return choices[0] if choices else (default or "auto")
    try:
        response = input(prompt).strip()
        if choices and response not in choices:
            print(f"Invalid. Options: {{choices}}")
            return safe_input(prompt, default, choices)
        return response if response else (default or "")
    except (EOFError, KeyboardInterrupt):
        return default or ""


